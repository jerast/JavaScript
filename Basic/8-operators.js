/** Asignación Operadores
 
 * =    Asignar un valor a una variable */ 

var x = 5; // 5

/** Comparación Operadores 
  
 * ==	Igual a
 * ===	Igual valor y tipo
 * !=	No igual a
 * !==	No igual valor y tipo
 * >	Mayor que
 * <	Menor que
 * >=	Mayor o igual que
 * <=	Menor o igual que */

if(a >= b) {} // Si `a` es mayor que `b` entonces ...

/** Aritméticos Operadores
 * +	Adición
 * -	Sustracción
 * *	Multiplicación
 * /	División
 * %	Módulo (resto de la división) */

x + 2; // 7

